#!/usr/bin/env python
from datetime import datetime, timedelta
import unittest
from app import create_app, db


class AuthenticationTest(unittest.TestCase):
    def setUp(self):
        self.app = create_app()
        self.app_context = self.app.app_context()
        self.app_context.push()
        db.create_all()

    def tearDown(self):
        db.session.remove()
        db.drop_all()
        self.app_context.pop()
